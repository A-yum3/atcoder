template<class T> inline bool chmax(T& a, T b) { if (a < b) { a = b; return 1; } return 0; }
template<class T> inline bool chmin(T& a, T b) { if (a > b) { a = b; return 1; } return 0; }

int ans;
int n;

// 現在値を保持、bit管理で7,5,3が1つ以上使われているか管理をする。
void dfs(ll now, int mask) {
    if(now > n) return;
    if(mask == 7) ans++;
    dfs(now * 10 + 7, mask | 4); // 3ビット目を立たせてる 0100
    dfs(now * 10 + 5, mask | 2); // 2          "        0010
    dfs(now * 10 + 3, mask | 1); // 1          "        0001
}

int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);

    cin >> n;
    dfs(0, 0);
    cout << ans << endl;
}
