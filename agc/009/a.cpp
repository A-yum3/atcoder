template<class T> inline bool chmax(T& a, T b) { if (a < b) { a = b; return 1; } return 0; }
template<class T> inline bool chmin(T& a, T b) { if (a > b) { a = b; return 1; } return 0; }

int main(){
    cin.tie(0);
    ios::sync_with_stdio(false);

    ll n; cin >> n;
    vector<ll> a(n);
    vector<ll> b(n);
    rep(i, n) {
        cin >> a[i] >> b[i];
    }
    ll ans = 0;
    for(int i = n - 1; i >= 0; i--) {
        ll tmp = 0;
        a[i] += ans;
        tmp = b[i] - (a[i] % b[i]);
        if(a[i] % b[i] == 0) continue;
        ans += tmp;
    }
    cout << ans << endl;
}
